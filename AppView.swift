import SwiftUI
import UniformTypeIdentifiers

// MARK: - App View

/// Root view. Switches between SetupView (no folder chosen) and the main
/// NavigationSplitView once a downloads folder has been configured.
struct AppView: View {
    @StateObject var manager = PodWangManager()
    @State private var selectedFeed: Feed?
    @State private var searchText = ""

    @State private var showingFilePicker   = false
    @State private var showingFileExporter = false
    @State private var opmlDoc: OPMLDocument?

    var body: some View {
        Group {
            if manager.isStorageConfigured {
                NavigationSplitView {
                    SidebarView(
                        manager: manager,
                        selectedFeed: $selectedFeed,
                        searchText: $searchText,
                        showingFilePicker: $showingFilePicker,
                        showingFileExporter: $showingFileExporter,
                        opmlDoc: $opmlDoc
                    )
                    .searchable(text: $searchText, placement: .sidebar, prompt: "Search Feeds")
                } detail: {
                    if let feed = selectedFeed {
                        EpisodeDetailView(manager: manager, feed: feed)
                    } else {
                        EmptyDetailView()
                    }
                }
                .navigationTitle(selectedFeed?.title ?? "PodWang")
            } else {
                SetupView(manager: manager)
            }
        }
        // File importer for OPML/XML import — presented by the sidebar gear menu.
        .fileImporter(
            isPresented: $showingFilePicker,
            allowedContentTypes: [.xml],
            allowsMultipleSelection: false
        ) { result in
            if let url = try? result.get().first { manager.importFromOPML(url: url) }
        }
        // File exporter for OPML/XML export — document is generated by the sidebar gear menu.
        .fileExporter(
            isPresented: $showingFileExporter,
            document: opmlDoc,
            contentType: .xml,
            defaultFilename: "PodWang Backup"
        ) { _ in }
    }
}

